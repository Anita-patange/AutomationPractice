public class upparcase {
	
	public static void main(String [] args) {
		
		for (char c1='a'; c1 <= 'z'; c1++) {
			System.out.println(c1);
			
		}
		for (char c2='A'; c2<='Z'; c2++) {
			System.out.println(c2);
		}
		for (char c3='z'; c3>='a'; c3--) {
			System.out.println(c3);
		}
	}

}
